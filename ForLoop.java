/**
*For loop: Simlar to While loop but the difference is in the syntax part. If you see while loop the initilization, condition and increment/decrement part is done
differently. So, in for loop all the stuff are put together.
Eg:
  for(initilization;condition;increment/decrement){
  statements;
}
*/

public class ForLoop{
  public static void main(String[] args) {
    for (int number=0;number<=10 ;number++ ) {
      System.out.print(number+" ");
    }
  }
}
